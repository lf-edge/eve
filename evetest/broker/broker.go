// ConnectConsoleToDevice establishes a bidirectional gRPC tunnel between
// the client and the console of a specific EVE device.
func (b *broker) ConnectConsoleToDevice(
	stream grpc.BidiStreamingServer[
		api.ConnectConsoleRequest, api.ConnectConsoleResponse]) error {

	// Receive the first message to identify the client and the target device.
	firstMsg, err := stream.Recv()
	if err != nil {
		err = fmt.Errorf("failed to receive initial connection request: %w", err)
		b.globalLog.Error(err)
		return err
	}
	connectReq := firstMsg.GetConnect()
	if connectReq == nil {
		err = fmt.Errorf("expected initial 'connect' request")
		b.globalLog.Error(err)
		return err
	}

	clientID := connectReq.GetClientId()
	devName := connectReq.GetDeviceName()

	b.mutex.Lock()
	clientSession, clientExists := b.sessions[clientID]
	b.mutex.Unlock()
	if !clientExists {
		err := clientNotFoundErr(clientID)
		b.globalLog.Error(err)
		return err
	}
	log := clientSession.log
	ctx := logger.WithLogger(stream.Context(), log)

	clientSession.mutex.Lock()
	eveDev, devExists := clientSession.eveDevices[devName]
	var providerDevName string
	if devExists {
		providerDevName = eveDev.providerDevName
	}
	clientSession.mutex.Unlock()
	if !devExists {
		err = eveDevNotFoundErr(devName)
		log.Error(err)
		return err
	}

	// Attach to provider’s console stream.
	consoleStream, echoed, telnet, err := b.provider.AttachToDeviceConsole(
		ctx, providerDevName)
	if err != nil {
		err = fmt.Errorf("failed to attach to console of device %q: %w", devName, err)
		log.Error(err)
		return err
	}
	defer consoleStream.Close()

	// Send console properties to the client.
	if err := stream.Send(&api.ConnectConsoleResponse{
		Payload: &api.ConnectConsoleResponse_ConnectReply{
			ConnectReply: &api.ConsoleProperties{
				Echoed: echoed,
				Telnet: telnet,
			},
		},
	}); err != nil {
		err = fmt.Errorf("failed to send console properties for %q: %v", devName, err)
		log.Error(err)
		return err
	}

	log.Infof("Console attached for EVE device %q (clientID=%s, echoed=%t, telnet=%t)",
		devName, clientID, echoed, telnet)

	// Run proxy between the EVE console and the gRPC client.
	grpcPipe := utils.GrpcServerPipe[api.ConnectConsoleRequest, api.ConnectConsoleResponse]{
		MakeResponse: func(data []byte) *api.ConnectConsoleResponse {
			return &api.ConnectConsoleResponse{
				Payload: &api.ConnectConsoleResponse_Data{
					Data: data,
				},
			}
		},
		Stream: stream,
	}
	consolePipe := utils.ReadWriterPipe{
		PipeName: "EVE console",
		RW:       consoleStream,
		Buf:      make([]byte, os.Getpagesize()),
	}
	utils.RunPipeProxy(ctx, log, "EVE console", consolePipe, grpcPipe)
	return nil
}

// sdnTunnelClientStream and sdnTunnelServerStream shorten the long generic
// instantiations used throughout the SDN tunnel connect/retry logic below.
type (
	sdnTunnelClientStream = grpc.BidiStreamingClient[
		api.ConnectTunnelToSDNRequest, api.ConnectTunnelToSDNResponse]
	sdnTunnelServerStream = grpc.BidiStreamingServer[
		api.ConnectTunnelToSDNRequest, api.ConnectTunnelToSDNResponse]
)

// connectSDNTunnel dials the SDN's gRPC endpoint at sdnAddr, opens the
// ConnectTunnel stream, forwards the client's initial connect request, and
// waits for the tunnel properties response. On any failure it cleans up and
// returns an error, so the caller can just retry the whole thing on the next
// attempt.
func (b *broker) connectSDNTunnel(
	ctx context.Context, sdnAddr string, initMsg *api.ConnectTunnelToSDNRequest) (
	conn *grpc.ClientConn,
	sdnStream sdnTunnelClientStream,
	resp *api.ConnectTunnelToSDNResponse,
	err error,
) {
	// Note: no I/O is performed by grpc.NewClient, connection to SDN
	// is established with the first RPC call, which is ConnectTunnel() below.
	conn, err = grpc.NewClient(sdnAddr,
		grpc.WithTransportCredentials(insecure.NewCredentials()))
	if err != nil {
		err = fmt.Errorf("failed to create SDN client: %w", err)
		return nil, nil, nil, err
	}
	sdnClient := api.NewSDNClient(conn)

	sdnStream, err = sdnClient.ConnectTunnel(ctx)
	if err != nil {
		_ = conn.Close()
		err = fmt.Errorf("failed to establish tunnel to SDN: %w", err)
		return nil, nil, nil, err
	}

	if err = sdnStream.Send(initMsg); err != nil {
		_ = conn.Close()
		err = fmt.Errorf("failed to send connect request: %w", err)
		return nil, nil, nil, err
	}

	resp, err = sdnStream.Recv()
	if err != nil {
		_ = conn.Close()
		err = fmt.Errorf("failed to receive tunnel properties: %w", err)
		return nil, nil, nil, err
	}

	return conn, sdnStream, resp, nil
}

// ConnectTunnelToSDN establishes a bidirectional gRPC tunnel for carrying
// raw IP packets between the client and the SDN.
// The tunnel allows full-duplex communication over a point-to-point link.
func (b *broker) ConnectTunnelToSDN(stream sdnTunnelServerStream) error {
	// Receive the first message from the client to extract the client ID.
	initMsg, err := stream.Recv()
	if err != nil {
		err = fmt.Errorf("failed to receive initial tunnel request: %w", err)
		b.globalLog.Error(err)
		return err
	}
	connectReq := initMsg.GetConnect()
	if connectReq == nil {
		err = fmt.Errorf("expected initial 'connect' request")
		b.globalLog.Error(err)
		return err
	}
	clientID := connectReq.GetClientId()

	b.mutex.Lock()
	clientSession, exists := b.sessions[clientID]
	b.mutex.Unlock()
	if !exists {
		err := clientNotFoundErr(clientID)
		b.globalLog.Error(err)
		return err
	}
	log := clientSession.log
	ctx := logger.WithLogger(stream.Context(), log)

	clientSession.mutex.Lock()
	var sdnIPs []net.IP
	if clientSession.sdnDevice != nil {
		sdnIPs = clientSession.sdnDevice.uplinkIPs
	}
	sdnDeviceSetUp := clientSession.sdnDevice != nil
	clientSession.mutex.Unlock()
	if !sdnDeviceSetUp {
		err := fmt.Errorf("SDN device is not set up for this session")
		log.Error(err)
		return err
	}
	if len(sdnIPs) == 0 {
		err := fmt.Errorf("SDN management IP is not available")
		log.Error(err)
		return err
	}

	// Connect to SDN gRPC endpoint and complete its initial handshake; try every
	// uplink IP, retrying until one succeeds or sdnConnectTimeout elapses -- the
	// SDN VM takes real wall-clock time to finish booting and its mgmt agent to
	// become ready, so early attempts can fail at any step, not just the dial.
	connectCtx, cancel := context.WithTimeout(ctx, sdnConnectTimeout)
	defer cancel()
	var (
		lastErr   error
		conn      *grpc.ClientConn
		sdnStream sdnTunnelClientStream
		resp      *api.ConnectTunnelToSDNResponse
	)
connectLoop:
	for {
		for _, sdnIP := range sdnIPs {
			select {
			case <-connectCtx.Done():
				break connectLoop
			default:
			}

			sdnAddr := net.JoinHostPort(sdnIP.String(), strconv.Itoa(int(b.sdnGrpcPort)))
			conn, sdnStream, resp, lastErr = b.connectSDNTunnel(ctx, sdnAddr, initMsg)
			if lastErr != nil {
				log.Warnf("Failed to connect to SDN gRPC at %s (will retry): %v",
					sdnAddr, lastErr)
				continue
			}

			log.Infof("Connected to SDN gRPC at %s", sdnAddr)
			break connectLoop
		}

		select {
		case <-connectCtx.Done():
			break connectLoop
		case <-time.After(sdnConnectRetryInterval):
		}
	}
	if conn == nil || sdnStream == nil {
		err = fmt.Errorf(
			"unable to connect to SDN gRPC service on any of the uplink IPs (%v): %w",
			sdnIPs, lastErr)
		log.Error(err)
		return err
	}
	defer conn.Close()
	defer sdnStream.CloseSend()

	// Forward the tunnel properties received from the SDN back to the client.
	if err := stream.Send(resp); err != nil {
		err = fmt.Errorf("failed to send SDN tunnel properties: %v", err)
		log.Error(err)
		return err
	}

	// Run proxy between the two gRPC streams.
	grpcPipe1 := utils.GrpcServerPipe[api.ConnectTunnelToSDNRequest, api.ConnectTunnelToSDNResponse]{
		MakeResponse: func(data []byte) *api.ConnectTunnelToSDNResponse {
			return &api.ConnectTunnelToSDNResponse{
				Payload: &api.ConnectTunnelToSDNResponse_Data{
					Data: data,
				},
			}
		},
		Stream: stream,
	}
	grpcPipe2 := utils.GrpcClientPipe[api.ConnectTunnelToSDNRequest, api.ConnectTunnelToSDNResponse]{
		MakeRequest: func(data []byte) *api.ConnectTunnelToSDNRequest {
			return &api.ConnectTunnelToSDNRequest{
				Payload: &api.ConnectTunnelToSDNRequest_Data{
					Data: data,
				},
			}
		},
		Stream: sdnStream,
	}

	log.Infof("SDN Tunnel established: tunnel-properties=%v, clientID=%s",
		resp.GetConnectReply(), clientID)
	utils.RunPipeProxy(ctx, log, "SDN tunnel", grpcPipe1, grpcPipe2)
	return nil
}

// sdnDockerImageName returns the SDN Docker image name corresponding
// to the given SDNConfig.
func sdnDockerImageName(sdnConf *api.SDNConfig) (string, error) {
	if sdnConf == nil {
		return "", fmt.Errorf("SDN config is nil")
	}
	if sdnConf.ImageRepo == "" {
		return "", fmt.Errorf("SDN image repo is not defined")
	}
	if sdnConf.ImageVersion == "" {
		return "", fmt.Errorf("SDN image version is not defined")
	}

	return fmt.Sprintf("%s:%s", sdnConf.ImageRepo, sdnConf.ImageVersion), nil
}
