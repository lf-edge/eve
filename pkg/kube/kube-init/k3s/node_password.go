// Node-password paths. var so tests can redirect; same convention
// as the rest of the k3s/ paths.go file.
//
// PersistNodePasswdFile lives under /var/lib, which is a bind-mount
// of /persist/vault/kube — it survives kube container restarts.
// RuntimeNodePasswdFile is on the kube container's tmpfs overlay
// and would otherwise be regenerated on each reboot, leaving the
// new password mismatched against the hash k3s stored in the
// cluster secret <hostname>.node-password.k3s.
//
// StaleNodePasswdFlag is set when SaveNodePassword detects a
// brownfield first boot (persist file absent => fresh password just
// generated by k3s). FixNodePasswordSecret consumes it.
var (
	PersistNodePasswdFile = "/var/lib/k3s-node-password"
	RuntimeNodePasswdFile = "/etc/rancher/node/password"
	StaleNodePasswdFlag   = "/var/lib/k3s-passwd-secret-stale"
)

// RestoreNodePassword copies the persisted k3s node password into
// the runtime location k3s expects, before k3s starts. On a brand-
// new device the persistent file does not exist yet and this is a
// no-op; k3s will then generate a fresh password and
// SaveNodePassword persists it after the API server is up.
//
// Mirrors restore_node_password() from pkg/kube/cluster-utils.sh
// (upstream commit 91b9589c1, "fix(kube): persist k3s node
// password across reboots").
func RestoreNodePassword() error {
	src := PersistNodePasswdFile
	data, err := os.ReadFile(src)
	if errors.Is(err, os.ErrNotExist) {
		return nil
	}
	if err != nil {
		return fmt.Errorf("read %s: %w", src, err)
	}
	dst := RuntimeNodePasswdFile
	if err := os.MkdirAll(filepath.Dir(dst), 0o755); err != nil {
		return fmt.Errorf("mkdir %s: %w", filepath.Dir(dst), err)
	}
	if err := os.WriteFile(dst, data, 0o600); err != nil {
		return fmt.Errorf("write %s: %w", dst, err)
	}
	log.Printf("restored node password from %s", src)
	return nil
}

// SaveNodePassword persists the runtime node password whenever it
// differs from (or is newer than) the persistent copy.
//
//   - Runtime missing: no-op (k3s has not generated one yet; the
//     supervisor is still coming up).
//   - Persistent matches runtime: no-op (steady state).
//   - Persistent missing: brownfield first boot under this fix —
//     touch StaleNodePasswdFlag so FixNodePasswordSecret deletes
//     the stale cluster secret once the API server is reachable.
//   - Persistent exists but differs: refresh it (covers cert
//     rotation, manual intervention, corruption).
//
// Safe to call on every steady-state tick; the equality check
// makes the common case a no-op.
//
// Mirrors save_node_password() from pkg/kube/cluster-utils.sh
// (upstream commit 91b9589c1).
func SaveNodePassword() error {
	runtime, err := os.ReadFile(RuntimeNodePasswdFile)
	if errors.Is(err, os.ErrNotExist) {
		return nil
	}
	if err != nil {
		return fmt.Errorf("read %s: %w", RuntimeNodePasswdFile, err)
	}
	persist, err := os.ReadFile(PersistNodePasswdFile)
	persistMissing := errors.Is(err, os.ErrNotExist)
	if err != nil && !persistMissing {
		return fmt.Errorf("read %s: %w", PersistNodePasswdFile, err)
	}
	if !persistMissing && bytes.Equal(runtime, persist) {
		return nil
	}
	if persistMissing {
		if err := os.WriteFile(StaleNodePasswdFlag, nil, 0o644); err != nil {
			return fmt.Errorf("touch %s: %w", StaleNodePasswdFlag, err)
		}
	}
	if err := os.WriteFile(PersistNodePasswdFile, runtime, 0o600); err != nil {
		return fmt.Errorf("write %s: %w", PersistNodePasswdFile, err)
	}
	log.Printf("persisted node password to %s", PersistNodePasswdFile)
	return nil
}

// FixNodePasswordSecret deletes the stale cluster secret
// <hostname>.node-password.k3s that a brownfield first boot leaves
// behind. The secret holds the hash of the OLD (pre-fix, never-
// persisted) password; the running node now uses a different
// password (the one we just persisted), so k3s logs
// NodePasswordValidationFailed until the secret is removed and
// re-generated by the controller.
//
// Gated by StaleNodePasswdFlag — no-op on every other boot. The
// caller is expected to invoke this from the per-tick health
// worker so it retries until the API server is reachable; on
// success the flag is cleared.
//
// Mirrors fix_node_password_secret() from pkg/kube/cluster-utils.sh
// (upstream commit 91b9589c1).
func FixNodePasswordSecret(ctx context.Context) error {
	if _, err := os.Stat(StaleNodePasswdFlag); errors.Is(err, os.ErrNotExist) {
		return nil
	} else if err != nil {
		return fmt.Errorf("stat %s: %w", StaleNodePasswdFlag, err)
	}
	hostname, err := os.Hostname()
	if err != nil {
		return fmt.Errorf("get hostname: %w", err)
	}
	if hostname == "" {
		return nil
	}
	secretName := hostname + ".node-password.k3s"
	out, err := kubectlx.CmdContext(ctx, "-n", "kube-system",
		"delete", "secret", secretName, "--ignore-not-found").CombinedOutput()
	if err != nil {
		return fmt.Errorf("delete secret %s: %w (output: %s)",
			secretName, err, string(out))
	}
	log.Printf("deleted stale node password secret %s for brownfield fix",
		secretName)
	if err := os.Remove(StaleNodePasswdFlag); err != nil && !errors.Is(err, os.ErrNotExist) {
		return fmt.Errorf("remove %s: %w", StaleNodePasswdFlag, err)
	}
	return nil
}
